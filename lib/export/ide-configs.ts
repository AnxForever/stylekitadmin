// ---------------------------------------------------------------------------
// Unified generator
// ---------------------------------------------------------------------------

export function generateIdeConfig(
  slug: string,
  format: IdeConfigFormat
): string | null {
  switch (format) {
    case "cursorrules":
      return generateCursorRules(slug);
    case "claude-rules":
      return generateClaudeRules(slug);
    case "windsurf-rules":
      return generateWindsurfRules(slug);
    case "generic":
      return generateGenericRules(slug);
  }
}

export function getIdeConfigFilename(
  slug: string,
  format: IdeConfigFormat
): string {
  switch (format) {
    case "cursorrules":
      return ".cursorrules";
    case "claude-rules":
      return `${slug}.md`;
    case "windsurf-rules":
      return `.windsurf-rules`;
    case "generic":
      return `${slug}-rules.md`;
  }
}

export function getIdeConfigMimeType(format: IdeConfigFormat): string {
  switch (format) {
    case "cursorrules":
      return "text/plain";
    case "claude-rules":
    case "windsurf-rules":
    case "generic":
      return "text/markdown";
  }
}
