// IDE Config File Export - Generate .cursorrules, Claude rules, Windsurf rules, etc.
// For integration with AI-powered coding tools

import type { DesignStyle } from "../styles";
import type { StyleTokens } from "../styles/tokens";
import { getStyleBySlug } from "../styles";
import { getStyleTokens } from "../styles/tokens-registry";
import { getStyleRecipes } from "../recipes";

export type IdeConfigFormat =
  | "cursorrules"
  | "claude-rules"
  | "windsurf-rules"
  | "generic";

interface StyleData {
  style: DesignStyle;
  tokens: StyleTokens | undefined;
}

function resolveStyleData(slug: string): StyleData | null {
  const style = getStyleBySlug(slug);
  if (!style) return null;
  const tokens = getStyleTokens(slug);
  return { style, tokens };
}

function formatDoList(items: string[]): string {
  return items.map((item) => `- ${item}`).join("\n");
}

function formatTokenReference(tokens: StyleTokens): string {
  const lines: string[] = [];
  lines.push(`- Font (heading): ${tokens.typography.heading}`);
  lines.push(`- Font (body): ${tokens.typography.body}`);
  lines.push(`- Border radius: ${tokens.border.radius}`);
  lines.push(`- Border width: ${tokens.border.width}`);
  lines.push(`- Border color: ${tokens.border.color}`);
  lines.push(`- Shadow (sm): ${tokens.shadow.sm}`);
  lines.push(`- Shadow (md): ${tokens.shadow.md}`);
  lines.push(`- Shadow (lg): ${tokens.shadow.lg}`);
  lines.push(`- Shadow (hover): ${tokens.shadow.hover}`);
  lines.push(`- Spacing (section): ${tokens.spacing.section}`);
  lines.push(`- Spacing (container): ${tokens.spacing.container}`);
  lines.push(`- Spacing (card): ${tokens.spacing.card}`);
  lines.push(`- Transition: ${tokens.interaction.transition}`);
  return lines.join("\n");
}

function formatColorPalette(style: DesignStyle): string {
  const lines: string[] = [];
  lines.push(`- Primary: ${style.colors.primary}`);
  lines.push(`- Secondary: ${style.colors.secondary}`);
  for (let i = 0; i < style.colors.accent.length; i++) {
    lines.push(`- Accent ${i + 1}: ${style.colors.accent[i]}`);
  }
  return lines.join("\n");
}

function formatForbiddenClasses(tokens: StyleTokens, limit = 15): string {
  if (!tokens.forbidden || tokens.forbidden.classes.length === 0) return "";
  const lines: string[] = [];
  for (const cls of tokens.forbidden.classes.slice(0, limit)) {
    const reason = tokens.forbidden.reasons[cls];
    lines.push(`- \`${cls}\`${reason ? `: ${reason}` : ""}`);
  }
  return lines.join("\n");
}

function formatRecipeSummary(slug: string): string {
  const recipes = getStyleRecipes(slug);
  if (!recipes) return "No component recipes available.";

  const lines: string[] = [];
  for (const [id, recipe] of Object.entries(recipes.recipes)) {
    const baseClasses = recipe.skeleton.baseClasses.join(" ");
    lines.push(`### ${recipe.name}`);
    lines.push(`- ID: ${id}`);
    lines.push(`- Base classes: \`${baseClasses}\``);

    const variantEntries = Object.entries(recipe.variants);
    if (variantEntries.length > 0) {
      lines.push("- Variants:");
      for (const [, v] of variantEntries.slice(0, 5)) {
        lines.push(`  - ${v.label}: \`${v.classes.join(" ")}\``);
      }
    }

    if (recipe.states?.hover) {
      lines.push(`- Hover: \`${recipe.states.hover.join(" ")}\``);
    }
    lines.push("");
  }
  return lines.join("\n");
}

// ---------------------------------------------------------------------------
// Cursor Rules (.cursorrules)
// ---------------------------------------------------------------------------

export function generateCursorRules(slug: string): string | null {
  const data = resolveStyleData(slug);
  if (!data) return null;
  const { style, tokens } = data;

  const sections: string[] = [];

  sections.push(`# Style: ${style.nameEn}
# Generated by StyleKit (https://stylekit.dev)
# Slug: ${style.slug}`);

  sections.push(`## Design Philosophy
${style.philosophy.split("\n\n")[0]}`);

  sections.push(`## Do's
${formatDoList(style.doList)}`);

  sections.push(`## Don'ts
${formatDoList(style.dontList)}`);

  sections.push(`## AI Rules
${style.aiRules}`);

  sections.push(`## Color Palette
${formatColorPalette(style)}`);

  if (tokens) {
    sections.push(`## Token Reference
${formatTokenReference(tokens)}`);

    const forbidden = formatForbiddenClasses(tokens);
    if (forbidden) {
      sections.push(`## Forbidden Classes
${forbidden}`);
    }
  }

  const recipeSummary = formatRecipeSummary(slug);
  if (recipeSummary !== "No component recipes available.") {
    sections.push(`## Component Guidelines
${recipeSummary}`);
  }

  sections.push(`---
Generated by StyleKit · https://stylekit.dev
Style: ${style.name} (${style.nameEn})`);

  return sections.join("\n\n");
}

